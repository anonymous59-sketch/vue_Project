<template>
<div id="myDIV" class="header">
  <h2>My To Do List</h2>
  <input type="text" id="myInput" placeholder="Title..." v-model="newTask" @keyup.enter="newElement">
  <span @click="newElement()" class="addBtn">Add</span>
</div>

<ul id="myUL">
  <li v-for="todo in todos" v-bind:class="{checked : todo.complete}" @click="todoCompleted(todo.no)">
    {{ todo.task }}
    <span class="close" @click.stop="delTodo(todo.no)">X</span> <!-- 이벤트 버블링 주의 -->
  </li>
</ul>
</template>

<script setup>
import {ref, reactive} from "vue";
const todos = reactive([
  { no: 1, task: "Hit the gym", complete: false},
  { no: 2, task: "Pay bills", complete: true},
  { no: 3, task: "Meet George", complete: false},
  { no: 4, task: "Buy eggs", complete: false},
  { no: 5, task: "Read a book", complete: false},
  { no: 6, task: "Organize office", complete: false},
]);

// todo del
const delTodo = (delNo) => {
  // 데이터의 넘버를 확인해서 index를 들고와야한다.
  const delIdx = todos.findIndex((todo) => todo.no == delNo);
  // findIndex가 true를 반환하면 해당하는 idx를 반환한다.
  todos.splice(delIdx, 1);
}

// todo class toggle
const todoCompleted = (selectedNo) => {
  // for(let i = 0; i < todos.length; i++) {
  //   let info = todos[i];
  //   if(info.no == selectedNo) {
  //     info.complete = !info.complete;
  //   }
  // }

  todos.forEach(info => {
    if(info.no == selectedNo) {
      info.complete = !info.complete;
    }
  })
};

const newTask = ref("");
const newElement = () => {
  if(!newTask.value) {
    alert('할 일을 입력해주세요');
    return;
  }
  const lastTodo = todos[todos.length - 1];
  const newNo = lastTodo.no + 1;
  let newTodo = {
    no: newNo, task: newTask.value, complete: false
  };
  todos.push(newTodo);
  newTask.value = "";
}
</script>

